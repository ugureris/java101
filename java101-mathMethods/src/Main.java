public class Main {
    public static void main(String[] args) {
        System.out.println(Math.abs(-100)); //mutlak değer
        System.out.println(Math.cbrt(8));  //Küpkök
        System.out.println(Math.ceil(2.1)); //Yukarı yuvarlama
        System.out.println(Math.floor(2.9));    //Aşağı yuvarlama
        System.out.println(Math.max(100,200));  //büyüğü bulma
        System.out.println(Math.pow(2,3));  //üs alma
        System.out.println(Math.round(10.49)); //yuvarlama float
        System.out.println(Math.signum(-100));  //negatif mi pozitif mi
        System.out.println(Math.sqrt(4));   //karekök

    }
}